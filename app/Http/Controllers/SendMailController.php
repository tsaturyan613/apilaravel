    public function send(StorePostRequest $request){

        $request->validated();

        $body = [
            'name' => $request->name,
            'email' => $request->email,
            'subject' => $request->subject,
            'message' => $request->message
        ];
        Mail::to('tsaturyan.artash@mail.ru')->send(new SendMail($body));

        return response()->json($body);

    }

